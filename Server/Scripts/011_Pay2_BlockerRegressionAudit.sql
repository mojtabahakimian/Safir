/* Read-only release gate. Execute after ScriptSqly on the customer's database clone.
   Engine v2 runs are strict. Legacy runs are reported separately and are never required
   to contain columns that did not exist when they were finalized. */
SET NOCOUNT ON;

IF NOT EXISTS(SELECT 1 FROM dbo.PAY2_RUN WHERE PAYROLL_ENGINE_VERSION>=2)
 THROW 52000,N'هیچ Run آزمایشی با موتور Snapshot نسخه 2 در Clone وجود ندارد.',1;

;WITH V2Detail AS
(
 SELECT D.* FROM dbo.PAY2_RUN_DETAIL D JOIN dbo.PAY2_RUN R ON R.RUN_ID=D.RUN_ID WHERE R.PAYROLL_ENGINE_VERSION>=2
), V2Rail AS
(
 SELECT RUN_ID,EMP_ID,
  MAX(CASE WHEN ITEM_CODE_SNAP='BASE_SAL' AND NOMINAL_AMOUNT IS NOT NULL THEN 1 ELSE 0 END) HAS_N,
  MAX(CASE WHEN ITEM_CODE_SNAP='BASE_SAL_B' AND AMOUNT IS NOT NULL THEN 1 ELSE 0 END) HAS_O,
  MAX(CASE WHEN ITEM_CODE_SNAP='SANOVAT_PAYE' AND NOMINAL_AMOUNT<>AMOUNT THEN 1 ELSE 0 END) DUAL_SEN
 FROM V2Detail GROUP BY RUN_ID,EMP_ID
)
SELECT * INTO #V2Rail FROM V2Rail;

IF EXISTS(SELECT 1 FROM dbo.PAY2_RUN_DETAIL D JOIN dbo.PAY2_RUN R ON R.RUN_ID=D.RUN_ID WHERE R.PAYROLL_ENGINE_VERSION>=2 AND
 (D.NOMINAL_AMOUNT IS NULL OR D.ITEM_CODE_SNAP IS NULL OR D.ITEM_TYPE_SNAP IS NULL OR D.CALC_BASIS_SNAP IS NULL OR D.INS_SUBJECT_AMOUNT IS NULL OR D.TAX_SUBJECT_AMOUNT IS NULL))
 THROW 52001,N'Snapshot یک ردیف Engine v2 ناقص است.',1;
IF EXISTS(SELECT 1 FROM #V2Rail WHERE HAS_N=0 OR HAS_O=0) THROW 52002,N'Run نسخه 2 فاقد یکی از دو ریل است.',1;
IF NOT EXISTS(SELECT 1 FROM dbo.PAY2_RUN_LINE RL JOIN dbo.PAY2_RUN R ON R.RUN_ID=RL.RUN_ID WHERE R.PAYROLL_ENGINE_VERSION>=2 AND RL.NOMINAL_DAYS<>RL.WORK_DAYS)
 THROW 52003,N'سناریوی Snapshot شده DAYS != DAYSB در Run نسخه 2 وجود ندارد.',1;
IF NOT EXISTS(SELECT 1 FROM #V2Rail WHERE DUAL_SEN=1) THROW 52004,N'سنوات متفاوت در دو ریل نسخه 2 وجود ندارد.',1;

-- Exact official/nominal gross and monetary subject reconciliation for v2 only.
IF EXISTS(SELECT 1 FROM dbo.PAY2_RUN_LINE RL JOIN dbo.PAY2_RUN R ON R.RUN_ID=RL.RUN_ID CROSS APPLY
 (SELECT SUM(CASE WHEN D.ITEM_TYPE_SNAP IN(1,2) AND D.ITEM_CODE_SNAP<>'BASE_SAL' THEN D.AMOUNT ELSE 0 END) V FROM dbo.PAY2_RUN_DETAIL D WHERE D.RUN_ID=RL.RUN_ID AND D.EMP_ID=RL.EMP_ID) X
 WHERE R.PAYROLL_ENGINE_VERSION>=2 AND ABS(ISNULL(X.V,0)+ISNULL(RL.ROUNDING_ADJ,0)-RL.GROSS_PAY)>1)
 THROW 52005,N'ناخالص رسمی Engine v2 با جزئیات رسمی تراز نیست.',1;
IF EXISTS(SELECT 1 FROM dbo.PAY2_RUN_LINE RL JOIN dbo.PAY2_RUN R ON R.RUN_ID=RL.RUN_ID CROSS APPLY
 (SELECT SUM(CASE WHEN D.ITEM_TYPE_SNAP IN(1,2) AND D.ITEM_CODE_SNAP<>'BASE_SAL_B' THEN D.NOMINAL_AMOUNT ELSE 0 END) V FROM dbo.PAY2_RUN_DETAIL D WHERE D.RUN_ID=RL.RUN_ID AND D.EMP_ID=RL.EMP_ID) X
 WHERE R.PAYROLL_ENGINE_VERSION>=2 AND ABS(ISNULL(X.V,0)-RL.NOMINAL_GROSS)>1)
 THROW 52006,N'ناخالص اسمی Engine v2 با جزئیات اسمی تراز نیست.',1;
IF EXISTS(SELECT 1 FROM dbo.PAY2_RUN_DETAIL D JOIN dbo.PAY2_RUN R ON R.RUN_ID=D.RUN_ID WHERE R.PAYROLL_ENGINE_VERSION>=2 AND (D.INS_SUBJECT_AMOUNT<0 OR D.TAX_SUBJECT_AMOUNT<0))
 THROW 52007,N'Snapshot مبلغ مشمول منفی است.',1;

DECLARE @Ceiling BIGINT=TRY_CAST((SELECT CFG_VALUE FROM dbo.PAY2_CONFIG WHERE CFG_KEY='INS_CEILING_MONTHLY') AS BIGINT);
IF @Ceiling IS NOT NULL AND EXISTS(SELECT 1 FROM dbo.PAY2_RUN_LINE RL JOIN dbo.PAY2_RUN R ON R.RUN_ID=RL.RUN_ID WHERE R.PAYROLL_ENGINE_VERSION>=2 AND INS_BASE>CAST(@Ceiling/30.0*NOMINAL_DAYS AS BIGINT)) THROW 52008,N'سقف بیمه بر DAYS اسمی نقض شده است.',1;
IF EXISTS(SELECT 1 FROM dbo.PAY2_RUN_LINE RL JOIN dbo.PAY2_RUN R ON R.RUN_ID=RL.RUN_ID WHERE R.PAYROLL_ENGINE_VERSION>=2 AND (INS_EMPLOYER_BASE IS NULL OR INS_UNEMPLOYMENT IS NULL OR INS_EMPLOYER<>INS_EMPLOYER_BASE+INS_UNEMPLOYMENT)) THROW 52009,N'Snapshot بیمه کارفرما نامعتبر است.',1;
IF NOT EXISTS(SELECT 1 FROM dbo.PAY2_RUN_DETAIL D JOIN dbo.PAY2_RUN R ON R.RUN_ID=D.RUN_ID WHERE R.PAYROLL_ENGINE_VERSION>=2 AND D.INS_SUBJECT<>D.TAX_SUBJECT) THROW 52010,N'سناریوی INS_SUBJECT != TAX_SUBJECT نسخه 2 وجود ندارد.',1;

IF EXISTS(SELECT 1 FROM dbo.PAY2_SANOVAT_MIGRATION_LOG WHERE (NOMINAL_BASE_BEFORE IS NOT NULL AND NOMINAL_BASE_BEFORE<>NOMINAL_BASE_AFTER+NOMINAL_SENIORITY) OR (OFFICIAL_BASE_BEFORE IS NOT NULL AND OFFICIAL_BASE_BEFORE<>OFFICIAL_BASE_AFTER+OFFICIAL_SENIORITY)) THROW 52011,N'تساوی Migration سنوات نقض شده است.',1;
IF EXISTS(SELECT 1 FROM dbo.PAY2_DECREE WHERE EFF_TO IS NOT NULL AND EFF_TO<EFF_FROM) THROW 52012,N'حکم با بازه تاریخی نامعتبر وجود دارد.',1;
IF EXISTS(SELECT 1 FROM dbo.PAY2_SANOVAT_MIGRATION_LOG L JOIN dbo.PAY2_DECREE D ON D.DEC_ID=L.NEW_DEC_ID WHERE D.IS_CONFIRMED=1 AND (D.CONFIRMED_BY IS NULL OR D.CONFIRMED_AT IS NULL)) THROW 52013,N'حکم Migration فاقد اطلاعات تأیید است.',1;

-- Legacy compatibility evidence is informational, not a v2 release failure.
SELECT R.RUN_ID,R.STATUS,COUNT(*) DETAIL_COUNT,
 SUM(CASE WHEN D.NOMINAL_AMOUNT IS NULL OR D.ITEM_CODE_SNAP IS NULL THEN 1 ELSE 0 END) LEGACY_INCOMPLETE_ROWS
FROM dbo.PAY2_RUN R LEFT JOIN dbo.PAY2_RUN_DETAIL D ON D.RUN_ID=R.RUN_ID
WHERE R.PAYROLL_ENGINE_VERSION IS NULL OR R.PAYROLL_ENGINE_VERSION<2
GROUP BY R.RUN_ID,R.STATUS ORDER BY R.RUN_ID;

SELECT N'PAY2 Engine v2 blocker release audit passed' RESULT;
DROP TABLE #V2Rail;

namespace Safir.Server.Services;

/// <summary>
/// Reads payroll report values exclusively from the effective snapshots of a run.
/// Legal output never falls back to current item configuration: an incomplete
/// historical snapshot is rejected by HAS_COMPLETE_NOMINAL_SNAPSHOT.
/// </summary>
internal static class Pay2PayrollSnapshotQuery
{
    internal const string Sql = @"
WITH Detail AS
(
    SELECT D.RUN_ID, D.EMP_ID,
           D.ITEM_CODE_SNAP AS ITEM_CODE, D.ITEM_NAME_SNAP AS ITEM_NAME,
           D.ITEM_TYPE_SNAP AS ITEM_TYPE,
           D.NOMINAL_AMOUNT AS NOMINAL_AMOUNT,
           D.NOMINAL_AMOUNT AS NOMINAL_SNAPSHOT,
           D.INS_SUBJECT_AMOUNT, D.TAX_SUBJECT_AMOUNT, D.INS_SUBJECT, D.TAX_SUBJECT
    FROM dbo.PAY2_RUN_DETAIL D
    WHERE D.RUN_ID = @runId
), Agg AS
(
    SELECT EMP_ID,
       SUM(CASE WHEN ITEM_CODE='BASE_SAL' THEN NOMINAL_AMOUNT ELSE 0 END) AS BASE_WAGE_MONTHLY,
       SUM(CASE WHEN ITEM_CODE='SANOVAT_PAYE' THEN NOMINAL_AMOUNT ELSE 0 END) AS SENIORITY_MONTHLY,
       SUM(CASE WHEN ITEM_CODE IN('BASE_SAL','SANOVAT_PAYE') THEN INS_SUBJECT_AMOUNT ELSE 0 END) AS INSURANCE_WAGE_MONTHLY,
       SUM(CASE WHEN ITEM_CODE IN('BASE_SAL','SANOVAT_PAYE') THEN TAX_SUBJECT_AMOUNT ELSE 0 END) AS TAXABLE_WAGE_MONTHLY,
       SUM(CASE WHEN ITEM_CODE IN('HOME','GROCERY','FAMILY_ALLOW') THEN INS_SUBJECT_AMOUNT ELSE 0 END) AS DIRECT_INSURANCE_BENEFITS,
       SUM(CASE WHEN ITEM_CODE IN ('HOME','GROCERY') THEN NOMINAL_AMOUNT ELSE 0 END) AS HOME_GROCERY_BENEFITS,
       SUM(CASE WHEN ITEM_CODE='FAMILY_ALLOW' THEN NOMINAL_AMOUNT ELSE 0 END) AS MARITAL_ALLOWANCE,
       SUM(CASE WHEN ITEM_CODE IN ('HOME','GROCERY') THEN INS_SUBJECT_AMOUNT ELSE 0 END) AS INS_HOME_GROCERY_BENEFITS,
       SUM(CASE WHEN ITEM_CODE='FAMILY_ALLOW' THEN INS_SUBJECT_AMOUNT ELSE 0 END) AS INS_MARITAL_ALLOWANCE,
       SUM(CASE WHEN ITEM_TYPE IN (1,2)
                 AND ITEM_CODE NOT IN ('BASE_SAL','BASE_SAL_B','SANOVAT_PAYE','HOME','GROCERY','FAMILY_ALLOW')
                THEN INS_SUBJECT_AMOUNT ELSE 0 END) AS OTHER_INSURANCE_SUBJECT_ITEMS,
       SUM(CASE WHEN ITEM_TYPE IN (1,2)
                 AND ITEM_CODE NOT IN ('BASE_SAL','BASE_SAL_B','SANOVAT_PAYE')
                THEN TAX_SUBJECT_AMOUNT ELSE 0 END) AS OTHER_TAXABLE_ITEMS,
       SUM(CASE WHEN ITEM_TYPE IN (1,2) AND ITEM_CODE <> 'BASE_SAL_B' THEN NOMINAL_AMOUNT ELSE 0 END) AS NOMINAL_GROSS
    FROM Detail GROUP BY EMP_ID
)
SELECT RL.EMP_ID, R.PAYROLL_ENGINE_VERSION,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.EMP_CODE ELSE E.EMP_CODE END AS EMP_CODE,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.FIRST_NAME ELSE E.FIRST_NAME END AS FIRST_NAME,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.LAST_NAME ELSE E.LAST_NAME END AS LAST_NAME,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.LAST_NAME+N' '+ES.FIRST_NAME ELSE E.LAST_NAME+N' '+E.FIRST_NAME END AS FULL_NAME,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.NATIONAL_CODE ELSE E.NATIONAL_CODE END AS NATIONAL_CODE,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.INS_CODE ELSE E.INS_CODE END AS INS_CODE,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.FATHER_NAME ELSE E.FATHER_NAME END AS FATHER_NAME,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.ID_NUMBER ELSE E.ID_NUMBER END AS ID_NUMBER,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.BIRTH_PLACE ELSE E.BIRTH_PLACE END AS BIRTH_PLACE,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.BIRTH_DATE ELSE E.BIRTH_DATE END AS BIRTH_DATE,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.GENDER ELSE E.GENDER END AS GENDER,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.NATIONALITY_SNAP ELSE E.NATIONALITY END AS NATIONALITY,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.MARITAL_SNAP ELSE E.MARITAL END AS MARITAL,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.MOBILE ELSE E.MOBILE END AS MOBILE,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.INS_TYPE_SNAP ELSE E.INS_TYPE END AS INS_TYPE,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.TAX_EXEMPT_SNAP ELSE E.TAX_EXEMPT END AS TAX_EXEMPT,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.JOB_CODE_SNAP ELSE J.JOB_CODE END AS JOB_CODE,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.JOB_NAME_SNAP ELSE J.JOB_NAME END AS JOB_NAME, P.PERIOD_DATE,
       RL.NOMINAL_DAYS AS INSURANCE_DAYS,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.HIRE_DATE_SNAP ELSE COALESCE(RL.HIRE_DATE_SNAP,E.HIRE_DATE) END AS HIRE_DATE,
       CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.FIRE_DATE_SNAP ELSE COALESCE(RL.FIRE_DATE_SNAP,E.FIRE_DATE) END AS FIRE_DATE,
       ISNULL(G.BASE_WAGE_MONTHLY,0) AS BASE_WAGE_MONTHLY,
       ISNULL(G.SENIORITY_MONTHLY,0) AS SENIORITY_MONTHLY,
       ISNULL(G.INSURANCE_WAGE_MONTHLY,0) AS INSURANCE_WAGE_MONTHLY,
       ISNULL(G.TAXABLE_WAGE_MONTHLY,0) AS TAXABLE_WAGE_MONTHLY,
       ISNULL(G.DIRECT_INSURANCE_BENEFITS,0) AS DIRECT_INSURANCE_BENEFITS,
       ISNULL(G.INS_HOME_GROCERY_BENEFITS,0) + ISNULL(G.INS_MARITAL_ALLOWANCE,0) AS DISPLAY_OTHER_BENEFITS,
       ISNULL(G.OTHER_INSURANCE_SUBJECT_ITEMS,0) AS OTHER_INSURANCE_SUBJECT_ITEMS,
       ISNULL(G.OTHER_TAXABLE_ITEMS,0) AS OTHER_TAXABLE_ITEMS,
       ISNULL(G.INS_HOME_GROCERY_BENEFITS,0) + ISNULL(G.OTHER_INSURANCE_SUBJECT_ITEMS,0) AS DBF_GENERAL_BENEFITS,
       ISNULL(G.INS_MARITAL_ALLOWANCE,0) AS MARITAL_ALLOWANCE,
       RL.NOMINAL_GROSS AS NOMINAL_GROSS,
       RL.INS_BASE, RL.INS_WORKER, RL.INS_EMPLOYER, RL.INS_EMPLOYER_BASE, RL.INS_UNEMPLOYMENT, RL.TAX_BASE, RL.TAX_AMOUNT,
       RL.LOAN_DED, RL.ADVANCE_DED, RL.OTHER_DED, RL.TOTAL_DED,
       CASE WHEN EXISTS(SELECT 1 FROM Detail X WHERE X.EMP_ID=RL.EMP_ID AND X.ITEM_CODE='BASE_SAL' AND X.NOMINAL_SNAPSHOT IS NOT NULL) THEN CAST(1 AS bit) ELSE CAST(0 AS bit) END AS HAS_NOMINAL_RAIL,
       CASE WHEN RL.NOMINAL_DAYS IS NOT NULL AND RL.NOMINAL_GROSS IS NOT NULL
                  AND NOT EXISTS(SELECT 1 FROM Detail X WHERE X.EMP_ID=RL.EMP_ID AND
                       (X.NOMINAL_SNAPSHOT IS NULL OR X.ITEM_CODE IS NULL OR (R.PAYROLL_ENGINE_VERSION>=3 AND X.ITEM_NAME IS NULL) OR X.ITEM_TYPE IS NULL OR X.INS_SUBJECT_AMOUNT IS NULL OR X.TAX_SUBJECT_AMOUNT IS NULL
                        OR (X.ITEM_CODE='BASE_SAL_B' AND (X.INS_SUBJECT_AMOUNT<>0 OR X.TAX_SUBJECT_AMOUNT<>0))))
            THEN CAST(1 AS bit) ELSE CAST(0 AS bit) END AS HAS_COMPLETE_NOMINAL_SNAPSHOT,
       CASE WHEN RL.INS_EMPLOYER_BASE IS NOT NULL AND RL.INS_UNEMPLOYMENT IS NOT NULL THEN CAST(1 AS bit) ELSE CAST(0 AS bit) END AS PREMIUM_SNAPSHOT_AVAILABLE,
       CASE WHEN R.PAYROLL_ENGINE_VERSION<3 OR ES.RUN_ID IS NOT NULL THEN CAST(1 AS bit) ELSE CAST(0 AS bit) END AS HAS_COMPLETE_EMP_SNAPSHOT,
       ISNULL(RL.ROUNDING_ADJ,0) AS ROUNDING_ADJ,
       RL.NOMINAL_GROSS - RL.TOTAL_DED AS NOMINAL_NET_PAYABLE
FROM dbo.PAY2_RUN_LINE RL
INNER JOIN dbo.PAY2_RUN R ON R.RUN_ID=RL.RUN_ID
INNER JOIN dbo.PAY2_PERIOD P ON P.PER_ID=R.PER_ID
LEFT JOIN dbo.PAY2_RUN_EMP_SNAPSHOT ES ON ES.RUN_ID=RL.RUN_ID AND ES.EMP_ID=RL.EMP_ID
LEFT JOIN dbo.PAY2_EMPLOYEE E ON E.EMP_ID=RL.EMP_ID AND R.PAYROLL_ENGINE_VERSION<3
LEFT JOIN dbo.PAY2_JOB J ON J.JOB_ID=E.JOB_ID AND R.PAYROLL_ENGINE_VERSION<3
LEFT JOIN Agg G ON G.EMP_ID=RL.EMP_ID
WHERE RL.RUN_ID=@runId
ORDER BY CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.LAST_NAME ELSE E.LAST_NAME END,
         CASE WHEN R.PAYROLL_ENGINE_VERSION>=3 THEN ES.FIRST_NAME ELSE E.FIRST_NAME END";
}
